# Read details like name, class, and age of a student.
# Print the details first in the same line, then in separate lines. 
# Ensure there are two blank lines between the outputs.
name = input("Enter the student's name: ")
student_class = input("Enter the student's class: ")
age = input("Enter the student's age: ")
# Print details in the same line
print(f"Name: {name}, Class: {student_class}, Age: {age}")
# Print details in separate lines
print("Name:", name, "\nClass:", student_class, "\nAge:", age)

